/**
 * Constants for the Magnet animation component
 */

/** Default padding around the element in pixels that activates the magnet effect */
export const DEFAULT_PADDING = 100;

/** Default state for whether the magnet effect is disabled */
export const DEFAULT_DISABLED = false;

/** Default strength of the magnetic effect (lower values = stronger pull) */
export const DEFAULT_MAGNET_STRENGTH = 2;

/** Default CSS transition value when magnetic effect is active */
export const DEFAULT_ACTIVE_TRANSITION = 'transform 0.3s ease-out';

/** Default CSS transition value when magnetic effect is inactive */
export const DEFAULT_INACTIVE_TRANSITION = 'transform 0.5s ease-in-out';

/** Default additional class name for the wrapper element */
export const DEFAULT_WRAPPER_CLASS_NAME = '';

/** Default additional class name for the inner element that moves */
export const DEFAULT_INNER_CLASS_NAME = '';

/** Default position with no offset */
export const DEFAULT_POSITION = { x: 0, y: 0 };
