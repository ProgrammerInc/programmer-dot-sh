const SidebarMenuSkeleton = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<'div'> & {
    showIcon?: boolean;
  }
>(({ className, showIcon = false, ...props }, ref) => {
  // Random width between 50 to 90%.
  const width = React.useMemo(() => {
    return `${Math.floor(Math.random() * 40) + 50}%`;
  }, []);

  React.useEffect(() => {
    // Find all skeleton text elements and set their width
    const skeletonElements = document.querySelectorAll('[data-sidebar="menu-skeleton-text"]');
    skeletonElements.forEach(el => {
      (el as HTMLElement).style.maxWidth = width;
    });
  }, [width]);

  return (
    <div
      ref={ref}
      data-sidebar="menu-skeleton"
      className={cn(styles.menuSkeleton, className)}
      {...props}
    >
      {showIcon && <Skeleton className={styles.menuSkeletonIcon} data-sidebar="menu-skeleton-icon" />}
      <Skeleton
        className={styles.menuSkeletonText}
        data-sidebar="menu-skeleton-text"
      />
    </div>
  );
});
SidebarMenuSkeleton.displayName = 'SidebarMenuSkeleton';

